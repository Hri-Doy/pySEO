str1 = "Santa islam runy"
str2 = "SANTA ISLAM RUNY"
print("Title: ", str1.title()) #First Lettle Of Each Word Will Be Capitalize
print("Capitalize case: ", str1.capitalize()) # First lettler of each sentence will be capitalized
print("Lower case: ", str2.lower()) # all letters will be lower case.
print("Upper case: ", str2.upper()) # all letters will be Upper case.
print("Casefold case: ", str2.casefold()) # all letters will be lower case. It forces to make a word lowercase.
print("Swap Case: ", str1.swapcase()) # Upper case will be lower and Lower call will be upper.